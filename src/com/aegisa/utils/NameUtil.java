    public static String getRandomName() {
        Random random = new Random();
        int count = 2;
        if (random.nextInt(10) + 1 >= 8) {
            count--;
        }
        String name = "";
        name += familyNames[random.nextInt(400)];
        for (int i = 0; i < count; i++) {
            name += String.valueOf(getRandomChar());
        }
        return name;
    }

